use similar::TextDiff;
use std::collections::{BTreeMap, BTreeSet};
use std::ffi::OsString;
use std::path::{Path, PathBuf};
use std::sync::mpsc;
use std::time::{Duration, Instant};
use typewriter_core::config::TypewriterConfig;
use typewriter_engine::drift::{self, DriftStatus};
use typewriter_engine::emit;
use typewriter_engine::plugin_registry;
use typewriter_engine::{
    Language, LanguageTarget, TypeSpec, all_languages, parse_languages, project, scan,
};

