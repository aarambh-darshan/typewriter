    fn parses_comma_separated_langs() {
        let parsed = parse_languages(&["typescript,python".to_string()]).unwrap();
        assert_eq!(
            parsed,
            vec![
                LanguageTarget::BuiltIn(Language::TypeScript),
                LanguageTarget::BuiltIn(Language::Python),
            ]
        );
    }

